-- Legal Documents table for storing privacy policy and terms of service
CREATE TABLE IF NOT EXISTS legal_documents (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Тип документа
    document_type VARCHAR(50) NOT NULL, -- 'privacy_policy' или 'terms_of_service'

    -- Версия
    version VARCHAR(20) NOT NULL, -- '1.0', '1.1', '2.0' и т.д.

    -- Содержимое
    title VARCHAR(500) NOT NULL,
    content TEXT NOT NULL, -- полный текст документа в markdown

    -- Метаданные
    language VARCHAR(10) NOT NULL DEFAULT 'ru', -- 'ru', 'en'
    is_active BOOLEAN NOT NULL DEFAULT TRUE, -- активная версия для показа
    effective_date DATE NOT NULL, -- дата вступления в силу

    -- Timestamps
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    -- Уникальность версии для каждого типа документа
    UNIQUE (document_type, version, language)
);

-- Email Verifications table for email confirmation flow
CREATE TABLE IF NOT EXISTS email_verifications (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Email
    email VARCHAR(255) NOT NULL,

    -- Verification code
    verification_code VARCHAR(6) NOT NULL, -- 6-значный код

    -- Status
    is_verified BOOLEAN NOT NULL DEFAULT FALSE,
    verified_at TIMESTAMPTZ,

    -- Expiration
    expires_at TIMESTAMPTZ NOT NULL, -- код действителен 15 минут

    -- Attempts
    send_attempts INT NOT NULL DEFAULT 1, -- кол-во отправок
    verify_attempts INT NOT NULL DEFAULT 0, -- кол-во попыток ввода кода

    -- Metadata
    parent_user_id VARCHAR(255), -- для связи с родителем
    platform_id VARCHAR(20), -- 'vk', 'telegram', etc.
    ip_address VARCHAR(45),

    -- Timestamps
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Индексы для legal_documents
CREATE INDEX IF NOT EXISTS idx_legal_documents_type_active
    ON legal_documents (document_type, is_active)
    WHERE is_active = TRUE;

CREATE INDEX IF NOT EXISTS idx_legal_documents_effective_date
    ON legal_documents (effective_date DESC);

-- Индексы для email_verifications
CREATE INDEX IF NOT EXISTS idx_email_verifications_email
    ON email_verifications (email);

CREATE INDEX IF NOT EXISTS idx_email_verifications_code
    ON email_verifications (verification_code);

CREATE INDEX IF NOT EXISTS idx_email_verifications_expires
    ON email_verifications (expires_at)
    WHERE is_verified = FALSE;

-- Триггеры для updated_at
CREATE TRIGGER legal_documents_updated_at
    BEFORE UPDATE ON legal_documents
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER email_verifications_updated_at
    BEFORE UPDATE ON email_verifications
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- Комментарии
COMMENT ON TABLE legal_documents IS 'Юридические документы (политика конфиденциальности, условия использования)';
COMMENT ON COLUMN legal_documents.document_type IS 'Тип: privacy_policy или terms_of_service';
COMMENT ON COLUMN legal_documents.is_active IS 'Активная версия для отображения пользователям';
COMMENT ON COLUMN legal_documents.effective_date IS 'Дата вступления в силу';

COMMENT ON TABLE email_verifications IS 'Верификация email адресов родителей';
COMMENT ON COLUMN email_verifications.verification_code IS '6-значный код для подтверждения';
COMMENT ON COLUMN email_verifications.expires_at IS 'Код действителен 15 минут';

